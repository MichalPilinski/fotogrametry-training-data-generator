#pragma once

#include "Light.h"

Light::Light(glm::vec3 position, Shader lightShader, Shader lampShader)
{
	Position = position;
	LightShader = lightShader;
	LampShader = lampShader;
}

void Light::SetVectors(glm::vec3 ambient, glm::vec3 diffuse, glm::vec3 specular)
{
	Ambient = ambient;
	Diffuse = diffuse;
	Specular = specular;
}

void Light::SetScalars(float constant, float linear, float quadratic)
{
	Constant = constant;
	Linear = linear;
	Quadratic = quadratic;
}

void Light::SetPosition(glm::vec3 position)
{
	Position = position;
}

void Light::Draw() 
{
	LampShader.use();
	glm::mat4 Transformation;
	Transformation = glm::translate(Transformation, Position);

	LampShader.setMat4("model", Transformation);
	glBindVertexArray(CubeVAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
}

